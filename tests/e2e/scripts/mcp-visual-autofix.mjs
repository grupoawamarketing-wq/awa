#!/usr/bin/env node
import fs from 'fs';
import path from 'path';

const runMode = process.env.MCP_AUTOFIX_MODE || 'dry-run';
function resolveProjectRoot() {
  const cwd = process.cwd();
  if (cwd.endsWith(path.join('tests', 'e2e'))) {
    return path.resolve(cwd, '..', '..');
  }
  return cwd;
}

const rootDir = resolveProjectRoot();
const consolidatedPath = path.join(rootDir, 'tests/e2e/reports/mcp-visual/mcp-visual-consolidated.json');
const targetLess = path.join(
  rootDir,
  'app/design/frontend/AWA_Custom/ayo_home5_child/web/css/source/_mcp-visual-autofix.less'
);

if (!fs.existsSync(consolidatedPath)) {
  console.error(`[mcp-visual-autofix] Relatorio nao encontrado: ${consolidatedPath}`);
  process.exit(1);
}

const data = JSON.parse(fs.readFileSync(consolidatedPath, 'utf8'));
const findings = (data.runs || []).flatMap((run) => run.findings || []);
const rules = new Set(
  findings
    .map((f) => f.autofixRuleId)
    .filter((v) => typeof v === 'string' && v && v !== 'manual-review-required')
);

const snippets = [];

if (rules.has('header-account-overlap')) {
  snippets.push(`
/* MCP autofix: header-account-overlap */
.page-header .header.content {
  display: grid;
  grid-template-columns: auto 1fr auto auto;
  align-items: center;
  gap: 12px;
}
`);
}

if (rules.has('text-clipping')) {
  snippets.push(`
/* MCP autofix: text-clipping */
.customer-welcome,
.authorization-link a,
.page-title-wrapper h1 {
  white-space: normal;
  overflow: visible;
  text-overflow: clip;
}
`);
}

if (rules.has('horizontal-overflow')) {
  snippets.push(`
/* MCP autofix: horizontal-overflow */
html,
body {
  overflow-x: clip;
}
.page-wrapper,
.columns,
.page-main {
  max-width: 100%;
}
`);
}

if (rules.has('cookie-banner-obstructive')) {
  snippets.push(`
/* MCP autofix: cookie-banner-obstructive */
.cookie-notice,
.cookie-message,
.awa-cookie-banner {
  max-height: 64px;
  overflow: auto;
}
@media (max-width: 768px) {
  .cookie-notice,
  .cookie-message,
  .awa-cookie-banner {
    max-height: 96px;
  }
}
`);
}

const banner = [
  '/**',
  ' * AUTO-GENERATED FILE - MCP Visual AutoFix',
  ' * Mode: generated by tests/e2e/scripts/mcp-visual-autofix.mjs',
  ' * Review before production deploy.',
  ' */',
  '',
].join('\n');

const content =
  snippets.length === 0
    ? `${banner}/* No autofix rules matched for current findings. */\n`
    : `${banner}${snippets.join('\n')}\n`;

if (runMode !== 'apply') {
  console.log('[mcp-visual-autofix] Dry-run mode. No file was changed.');
  console.log(`[mcp-visual-autofix] Candidate output: ${targetLess}`);
  console.log(content);
  process.exit(0);
}

fs.mkdirSync(path.dirname(targetLess), { recursive: true });
fs.writeFileSync(targetLess, content, 'utf8');
console.log(`[mcp-visual-autofix] Applied: ${targetLess}`);
